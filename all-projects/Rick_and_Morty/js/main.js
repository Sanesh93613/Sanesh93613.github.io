// ============================================================================
// ВНИМАНИЕ! Этот второй вызов НЕ ЯВЛЯЕТСЯ ОШИБКОЙ!
//
// В старом проекте было два одинаковых блока запуска:
//   window.addEventListener("resize", onResize);
//   updateStage();
//   loop();
