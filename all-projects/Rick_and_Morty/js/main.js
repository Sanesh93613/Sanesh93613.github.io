// ============================================================================
// НЛО И ДЫМ — ОСНОВНАЯ ЛОГИКА
// ============================================================================
// Этот файл отвечает за всё, что связано с НЛО и дымом:
//
// 1. НАСТРОЙКИ ПАРАМЕТРОВ
//    - Скорость НЛО (squidSpeedFactor)
//    - Размер НЛО (радиус, ширина, высота)
//    - Густота, размер, разлёт и цвет дыма
//    - Расстояния для поворота и остановки дыма
//
// 2. ДВИЖЕНИЕ НЛО
//    - НЛО следует за курсором мыши
//    - Поворачивается в сторону движения
//    - Плавно тормозит при приближении к курсору
//
// 3. ГЕНЕРАЦИЯ ДЫМА
//    - Дым идёт из двух сопел (левое и правое)
//    - Частицы разлетаются, уменьшаются и исчезают
//    - Цвет дыма можно менять через настройки
//
// 4. ОТРИСОВКА
//    - Canvas, на котором рисуется НЛО и дым
//    - Картинка НЛО загружается из images/parallax/spaceman/jetpack.png
//
// 5. СОХРАНЕНИЕ НАСТРОЕК
//    - Все параметры можно менять через панель настроек
//    - Изменения сохраняются в localStorage
//
// 6. ДВОЙНОЙ ЗАПУСК (ВАЖНО!)
//    - В конце файла есть два одинаковых блока запуска
//    - Это НЕ ОШИБКА, а особенность старого проекта
//    - Без второго запуска НЛО летает медленнее
//    - НЕ УДАЛЯТЬ!
// ============================================================================

// ============================================================================
// 1. ГЛОБАЛЬНЫЕ ПЕРЕМЕННЫЕ
// ============================================================================

// Цвет дыма по умолчанию — белый (формат RGBA)
// Можно менять через панель настроек
let smokeColor = "rgba(255, 255, 255, 1)";

// Получаем элемент canvas (холст, на котором всё рисуется)
const canvas = document.querySelector("canvas");
// Получаем контекст для 2D-рисования (позволяет рисовать круги, картинки и т.д.)
const ctx = canvas.getContext("2d");

// 2 * π (6.283185...) — полный круг в радианах
// Используется для рисования круглых частиц дыма
const PI2 = Math.PI * 2;

// Объект, хранящий координаты курсора мыши
// x, y — позиция мыши на экране
// angle — угол поворота (используется для направления НЛО)
const mouse = { x: 0, y: 0, angle: 0 };

// ============================================================================
// 2. ВСПОМОГАТЕЛЬНЫЕ МАТЕМАТИЧЕСКИЕ ФУНКЦИИ
// ============================================================================

// Расстояние между двумя точками (теорема Пифагора)
// Принимает два объекта с полями x и y
// Возвращает расстояние в пикселях
// Используется для:
//   - определения дистанции от НЛО до курсора
//   - проверки, нужно ли генерировать дым
const distanceBetween = (p1, p2) =>
    Math.sqrt((p1.x - p2.x) * (p1.x - p2.x) + (p1.y - p2.y) * (p1.y - p2.y));

// Угол между двумя точками (в радианах)
// Принимает координаты двух точек (x1,y1) и (x2,y2)
// Возвращает угол от 0 до 2π
// Используется для поворота НЛО в сторону курсора
const angleBetween = (x1, y1, x2, y2) => Math.atan2(y2 - y1, x2 - x1);

// Случайное целое число между min и max (включительно)
// Принимает min и max
// Возвращает случайное число
// Используется для разброса частиц дыма в разные стороны
const randomBetween = (min, max) => ~~(Math.random() * (max - min + 1) + min);

// ============================================================================
// 3. ПЕРЕМЕННЫЕ ДЛЯ РАЗМЕРОВ ЭКРАНА
// ============================================================================

let w, wH; // w = ширина окна, wH = половина ширины (centerX)
let h, hH; // h = высота окна, hH = половина высоты (centerY)

// ============================================================================
// 4. ПАРАМЕТРЫ НЛО (значения по умолчанию)
// ============================================================================

// Объект НЛО — будет содержать x, y, radius, angle
let squid;

// Радиус НЛО (базовый размер картинки)
// При изменении этого параметра НЛО становится больше или меньше
let squidRadius = 20;

// Фактор ширины — умножается на радиус, чтобы получить итоговую ширину
// squidWidth = squidRadius * squidWidthFactor
// Больше значение = шире НЛО
let squidWidthFactor = 15;

// Фактор высоты — умножается на радиус, чтобы получить итоговую высоту
// squidHeight = squidRadius * squidHeightFactor
// Больше значение = выше НЛО
let squidHeightFactor = 12;

// ============================================================================
// 5. ПАРАМЕТРЫ ДВИЖЕНИЯ
// ============================================================================

// Скорость НЛО (фактор деления)
// ФОРМУЛА: newX = squid.x + (mouse.x - squid.x) / squidSpeedFactor
// ЧЕМ МЕНЬШЕ ЧИСЛО, ТЕМ БЫСТРЕЕ НЛО!
// 50  = очень быстро
// 100 = стандартная скорость (рекомендуется)
// 200 = медленно
// 500 = еле ползёт
let squidSpeedFactor = 100;

// Порог дистанции для дыма
// Если расстояние от НЛО до курсора МЕНЬШЕ этого значения — дым НЕ генерируется
// 200 пикселей = дым перестаёт идти, когда НЛО близко к курсору
let cursorDistanceThreshold = 200;

// Порог дистанции для поворота
// Если расстояние от НЛО до курсора БОЛЬШЕ этого значения — НЛО поворачивается
// Если МЕНЬШЕ — угол сбрасывается в 0 (НЛО смотрит прямо)
let squidDistanceThreshold = 800;

// ============================================================================
// 6. ПАРАМЕТРЫ ДЫМА
// ============================================================================

// Размер каждой частицы дыма в пикселях
// 15 = средний размер (рекомендуется)
// 30 = крупные клубы
// 5  = мелкие точки
let smokeSize = 15;

// Частота генерации дыма — количество частиц за один вызов generateSmoke()
// 5 = нормальная густота
// 1 = редкий дым
// 10 = очень густой
let smokeFrequency = 5;

// Скорость угасания дыма (уменьшение жизни частицы за кадр)
// 0.005 = дым живёт около 200 кадров (~3 секунды при 60 FPS)
// 0.01  = дым живёт около 100 кадров (~1.5 секунды)
// 0.001 = дым живёт очень долго
let smokeFade = 0.005;

// Разлёт частиц дыма (насколько широко они разлетаются в стороны)
// 0.5 = узкая струя
// 2   = широкое облако
let smokeSpread = 0.5;

// ============================================================================
// 7. АДАПТАЦИЯ ПОД РАЗМЕР ЭКРАНА
// ============================================================================

// Функция вызывается при изменении размера окна
// Обновляет размеры canvas и пересчитывает центр экрана
const onResize = () => {
    w = window.innerWidth; // Текущая ширина окна
    h = window.innerHeight; // Текущая высота окна

    // Битовый сдвиг вправо на 1 === деление на 2
    // wH = Math.floor(w / 2) — центр экрана по X
    wH = w >> 1;
    // hH = Math.floor(h / 2) — центр экрана по Y
    hH = h >> 1;

    // Устанавливаем размеры canvas в пикселях
    canvas.width = w;
    canvas.height = h;
};

// Инициализация сцены: ставим НЛО в центр экрана
// Также устанавливаем начальное положение мыши (на случай, если мышь ещё не двигалась)
const updateStage = () => {
    onResize(); // Обновляем размеры
    mouse.x = wH; // Ставим мышь в центр по X
    mouse.y = hH; // Ставим мышь в центр по Y
    squid = {
        x: mouse.x, // НЛО в центре по X
        y: mouse.y, // НЛО в центре по Y
        radius: 20, // Радиус НЛО
        angle: 0, // Начальный угол (смотрит прямо)
    };
};

// ============================================================================
// 8. ДВИЖЕНИЕ НЛО (СЛЕДОВАНИЕ ЗА КУРСОРОМ)
// ============================================================================

const updateSquid = () => {
    // --- РАСЧЁТ УГЛА ПОВОРОТА ---
    // Вычисляем угол от НЛО до курсора
    let targetAngle = angleBetween(squid.x, squid.y, mouse.x, mouse.y);
    const maxTilt = Math.PI / 6; // Максимальный угол наклона = 30 градусов
    const distance = distanceBetween(squid, mouse); // Расстояние до курсора

    // Корректируем угол в зависимости от положения курсора относительно НЛО
    if (distance < squidDistanceThreshold) {
        targetAngle = 0; // Близко к курсору — не наклоняем
    } else {
        // Определяем четверть, в которой находится курсор
        if (mouse.x < squid.x && mouse.y > squid.y) {
            targetAngle = -Math.abs(targetAngle); // Курсор слева снизу
        } else if (mouse.x > squid.x && mouse.y > squid.y) {
            targetAngle = Math.abs(targetAngle); // Курсор справа снизу
        } else if (mouse.x < squid.x && mouse.y < squid.y) {
            targetAngle = -Math.abs(targetAngle); // Курсор слева сверху
        } else if (mouse.x > squid.x && mouse.y < squid.y) {
            targetAngle = Math.abs(targetAngle); // Курсор справа сверху
        }
    }

    // Ограничиваем угол, чтобы НЛО не наклонялся слишком сильно
    targetAngle = Math.max(-maxTilt, Math.min(maxTilt, targetAngle));

    // --- ПЛАВНЫЙ ПОВОРОТ НЛО ---
    // Проверяем, стоит ли НЛО на месте (разница меньше 1 пикселя)
    const isStationary =
        Math.abs(mouse.x - squid.x) < 1 && Math.abs(mouse.y - squid.y) < 1;

    if (isStationary) {
        // НЛО стоит — медленно возвращаем угол к 0
        const distanceToZero = Math.abs(squid.angle);
        const angleStep =
            Math.sign(0 - squid.angle) *
            Math.min(distanceToZero, Math.PI / 720);
        squid.angle += angleStep;
    } else {
        // НЛО движется — плавно поворачиваем к цели
        const angleStep =
            Math.sign(targetAngle - squid.angle) *
            Math.min(Math.abs(targetAngle - squid.angle), Math.PI / 360);
        squid.angle += angleStep;
    }

    // --- ДВИЖЕНИЕ К КУРСОРУ ---
    // Формула: новая_позиция = старая_позиция + (расстояние_до_мыши / скорость)
    // Чем МЕНЬШЕ squidSpeedFactor, тем БЫСТРЕЕ НЛО
    const newX = squid.x + (mouse.x - squid.x) / squidSpeedFactor;
    const newY = squid.y + (mouse.y - squid.y) / squidSpeedFactor;

    // Если НЛО сдвинулся хотя бы на 0.1 пикселя — генерируем дым
    if (Math.abs(newX - squid.x) > 0.1 || Math.abs(newY - squid.y) > 0.1) {
        generateSmoke();
    }

    // Обновляем позицию НЛО
    squid.x = newX;
    squid.y = newY;
};

// ============================================================================
// 9. РИСОВАНИЕ НЛО
// ============================================================================

const drawSquid = () => {
    // Сохраняем текущее состояние canvas (матрицу трансформации)
    ctx.save();

    // Смещаем начало координат в центр НЛО
    ctx.translate(squid.x, squid.y);

    // Поворачиваем canvas на угол НЛО
    ctx.rotate(squid.angle);

    // Вычисляем итоговые размеры картинки
    const squidWidth = squid.radius * squidWidthFactor;
    const squidHeight = squid.radius * squidHeightFactor;

    // Рисуем картинку НЛО
    // -squidWidth/2 и -squidHeight/2 — центрируем картинку относительно (0,0)
    ctx.drawImage(
        squidImage,
        -squidWidth / 2, // X: сдвигаем на половину ширины влево
        -squidHeight / 2, // Y: сдвигаем на половину высоты вверх
        squidWidth, // Ширина картинки
        squidHeight, // Высота картинки
    );

    // Восстанавливаем состояние canvas (отменяем смещение и поворот)
    ctx.restore();
};

// Загружаем картинку НЛО из папки images
const squidImage = new Image();
squidImage.src = "./images/parallax/spaceman/jetpack.png";

// ============================================================================
// 10. ГЕНЕРАЦИЯ И ОТРИСОВКА ДЫМА
// ============================================================================

// Массив для хранения всех частиц дыма
let smokeParticles = [];

// Создание новой частицы дыма
// x, y — координаты, где появится частица
const createSmokeParticle = (x, y) => {
    smokeParticles.push({
        x: x, // Координата X
        y: y, // Координата Y
        life: 1, // Жизнь (от 1 до 0, когда 0 — удаляем)
        radius: smokeSize, // Радиус частицы
        velocityX: (Math.random() - 0.5) * smokeSpread, // Скорость по X (случайная)
        velocityY: Math.random() * -1, // Скорость по Y (вверх, отрицательное значение)
        opacity: 1, // Прозрачность (1 = непрозрачно)
    });
};

// Генерация дыма из двух сопел (левого и правого)
const generateSmoke = () => {
    // Не генерируем дым, если НЛО слишком близко к курсору
    const distanceToCursor = distanceBetween(squid, mouse);
    if (distanceToCursor < cursorDistanceThreshold) return;

    const angle = squid.angle;

    // --- ЛЕВОЕ СОПЛО (смещено влево относительно центра НЛО) ---
    // Math.cos(angle) и Math.sin(angle) — направление движения НЛО
    // Умножаем на радиус и на 5.0, чтобы сместить сопло дальше от центра
    const leftX = squid.x - Math.cos(angle) * squid.radius * 5.0;
    const leftY = squid.y - Math.sin(angle) * squid.radius * 5.0 + 20;

    // --- ПРАВОЕ СОПЛО (смещено вправо относительно центра НЛО) ---
    const rightX = squid.x + Math.cos(angle) * squid.radius * 5.0;
    const rightY = squid.y + Math.sin(angle) * squid.radius * 5.0 + 20;

    // Создаём smokeFrequency частиц для левого сопла и столько же для правого
    for (let i = 0; i < smokeFrequency; i++) {
        // Левое сопло с разбросом частиц
        createSmokeParticle(
            leftX + (Math.random() - 0.5) * smokeSpread,
            leftY + (Math.random() - 0.5) * smokeSpread,
        );
        // Правое сопло с разбросом частиц
        createSmokeParticle(
            rightX + (Math.random() - 0.5) * smokeSpread,
            rightY + (Math.random() - 0.5) * smokeSpread,
        );
    }
};

// Обновление цвета дыма (вызывается из настроек)
// Принимает цвет в HEX-формате, например "#ff0000"
const updateSmokeColor = (color) => {
    // Преобразуем HEX в число (например "#ff0000" → 16711680)
    const bigint = parseInt(color.slice(1), 16);
    // Извлекаем красный компонент (биты 16-23)
    const r = (bigint >> 16) & 255;
    // Извлекаем зелёный компонент (биты 8-15)
    const g = (bigint >> 8) & 255;
    // Извлекаем синий компонент (биты 0-7)
    const b = bigint & 255;
    // Сохраняем цвет в формате RGBA (альфа = 1, полная непрозрачность)
    smokeColor = `rgba(${r}, ${g}, ${b}, 1)`;
};

// Обновление позиций всех частиц дыма и их отрисовка
const updateAndDrawSmoke = () => {
    // Проходим по всем частицам в обратном порядке (чтобы безопасно удалять)
    for (let i = 0; i < smokeParticles.length; i++) {
        const p = smokeParticles[i];

        // Движение частицы (добавляем скорость к координатам)
        p.x += p.velocityX;
        p.y += p.velocityY;

        // Уменьшаем жизнь частицы
        p.life -= smokeFade;
        // Обновляем прозрачность в зависимости от оставшейся жизни
        p.opacity = Math.max(p.life, 0);
        // Радиус уменьшается пропорционально жизни
        p.radius = Math.max(smokeSize * p.life, 1);

        // Рисуем частицу как круг
        ctx.beginPath();
        // Заменяем "1)" в строке rgba на текущую прозрачность
        ctx.fillStyle = smokeColor.replace("1)", `${p.opacity})`);
        ctx.arc(p.x, p.y, p.radius, 0, PI2);
        ctx.fill();

        // Если жизнь частицы закончилась — удаляем её из массива
        if (p.life <= 0) {
            smokeParticles.splice(i, 1);
            i--; // Уменьшаем счётчик, потому что массив сжался
        }
    }
};

// ============================================================================
// 11. ОЧИСТКА CANVAS
// ============================================================================

// Очищает весь canvas (заливает прозрачным цветом)
const clear = () => ctx.clearRect(0, 0, canvas.width, canvas.height);

// ============================================================================
// 12. ГЛАВНЫЙ ЦИКЛ АНИМАЦИИ
// ============================================================================

const loop = () => {
    clear(); // 1. Очищаем canvas от предыдущего кадра
    updateAndDrawSmoke(); // 2. Обновляем позиции дыма и рисуем его
    updateSquid(); // 3. Обновляем позицию и угол НЛО
    drawSquid(); // 4. Рисуем НЛО
    requestAnimationFrame(loop); // 5. Запрашиваем следующий кадр (60 FPS)
};

// ============================================================================
// 13. ПЕРВЫЙ ЗАПУСК
// ============================================================================

// Подписываемся на событие изменения размера окна
window.addEventListener("resize", onResize);

// Инициализируем сцену (ставим НЛО в центр)
updateStage();

// Запускаем анимацию
loop();

// ============================================================================
// 14. ОТСЛЕЖИВАНИЕ ДВИЖЕНИЯ МЫШИ
// ============================================================================

// Обработчик движения мыши (поддерживает как мышь, так и сенсорные экраны)
const onPointerMove = (e) => {
    // Если есть касания (touchscreen) — берём первое касание
    const target = e.touches && e.touches.length ? e.touches[0] : e;
    mouse.x = target.clientX; // Координата X курсора
    mouse.y = target.clientY; // Координата Y курсора
};

// Вешаем обработчики на canvas
canvas.addEventListener("mousemove", onPointerMove);
canvas.addEventListener("touchmove", onPointerMove);

// ============================================================================
// 15. ГЛОБАЛЬНЫЕ ФУНКЦИИ ДЛЯ НАСТРОЕК
// ============================================================================
// Эти функции вызываются из settings.js при изменении ползунков

// Делаем функцию смены цвета дыма доступной глобально
window.updateSmokeColor = updateSmokeColor;

// Делаем функцию обновления всех параметров НЛО доступной глобально
window.updateSquidParams = (params) => {
    // Параметры НЛО
    if (params.squidRadius !== undefined) {
        squidRadius = params.squidRadius;
        squid.radius = squidRadius; // Обновляем радиус у текущего объекта
    }
    if (params.squidWidthFactor !== undefined)
        squidWidthFactor = params.squidWidthFactor;
    if (params.squidHeightFactor !== undefined)
        squidHeightFactor = params.squidHeightFactor;

    // Параметры движения
    if (params.squidDistanceThreshold !== undefined)
        squidDistanceThreshold = params.squidDistanceThreshold;
    if (params.squidSpeedFactor !== undefined)
        squidSpeedFactor = params.squidSpeedFactor;

    // Параметры дыма
    if (params.smokeSize !== undefined) smokeSize = params.smokeSize;
    if (params.smokeFrequency !== undefined)
        smokeFrequency = params.smokeFrequency;
    if (params.smokeFade !== undefined) smokeFade = params.smokeFade;
    if (params.smokeSpread !== undefined) smokeSpread = params.smokeSpread;
    if (params.cursorDistanceThreshold !== undefined)
        cursorDistanceThreshold = params.cursorDistanceThreshold;
};

// Делаем объект squid доступным глобально (для отладки)
window.squid = squid;

// Сообщаем в консоль, что всё загружено
console.log("✅ Первый работающий проект скопирован один в один!");

